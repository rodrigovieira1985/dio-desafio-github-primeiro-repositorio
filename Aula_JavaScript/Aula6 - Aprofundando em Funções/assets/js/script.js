//Tipos de Função

//Declarativas
function funcaoD(){
    console.log("Funcao Declarativa Funcionando");
}
funcaoD();

//Expressões de Funções

// Com nomeação exemplo 1
var funcaoEn = function funcaoEn(){
    console.log("Exemplo 1: Expessões de Função com Nomeação Funcionando");
}
funcaoEn();

// Com nomeação exemplo 2
var funcaoE = function(){
    console.log("Exemplo 2: Expessões de Função com Nomeação Funcionando");
}
funcaoE();

//Arrow Function
var funcaoA = () => {
    console.log("Funcao Arrow Function");
}
funcaoA();